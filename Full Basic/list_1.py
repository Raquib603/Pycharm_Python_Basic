### List is a collection which is ordered and changeable. Allows duplicate members

a = ['apple', 'banana', 'orange', 'cherry']
print(a)

print(a[-1])
print(a[:2])
print(a[2:4])
print(a[0])
print(len(a))

# list can contain different data types

b = [1, 'raquib', 4.0, True]
print(b)
print(type(b))
